//1 close to perfect
(function() {
                
    var aboutEl = $('div.paralex'),
    aboutElOffset = aboutEl.offset().top/1.2,
    documentEl = $(document);
                
    documentEl.on('scroll', function() {
    if ( documentEl.scrollTop() > aboutElOffset && aboutEl.hasClass('hidden') ) aboutEl.removeClass('hidden'); 
    });
    
})();

